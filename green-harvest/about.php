contributing about 7.4% to the national Gross Domestic Product (GDP). At Green Harvest, we're passionate about cultivating a greener future through sustainable agriculture practices. Our web application is designed to empower farmers, gardeners, and agricultural enthusiasts with the tools and knowledge they need to grow healthy, abundant crops while minimizing environmental impact.
      </h5>

      <h4 class="mission">Our Mission</h4>
      <h5>
      "At Green Harvest, our mission is to empower individuals and communities to embrace sustainable agriculture practices for a healthier planet. Through our innovative web application, we strive to provide accessible tools, resources, and knowledge that enable farmers and gardeners to cultivate abundant crops while preserving and enhancing the natural environment."
      </h5>

      <h4 class="mission">Our Vision</h4>
      <h5>
      "Our vision is a world where sustainable agriculture is the cornerstone of food production, fostering biodiversity, nourishing communities, and mitigating climate change. By fostering a global community of environmentally conscious growers, Green Harvest aims to catalyze a paradigm shift towards regenerative farming practices that sustainably feed future generations while respecting the Earth's finite resources."
      </h5>

      <h4 class="mission">Our Features</h4>
      <ul>
        <li>Crop Planning: Plan your crops efficiently using our intuitive crop planning tool. Select from a wide variety of crops, and we'll provide you with detailed information on planting times, spacing, and companion planting suggestions.</li>
        <li>Sustainable Practices: Discover eco-friendly farming techniques and practices that promote soil health, conserve water, and reduce reliance on chemical inputs. From organic pest control methods to water-saving irrigation techniques, we've got you covered.</li>
        <li>Community Forum: Connect with like-minded individuals, exchange ideas, and seek advice from experienced farmers and experts in our vibrant community forum. Share your success stories, ask questions, and learn from others on the journey towards sustainable agriculture.</li>
        <li>Marketplace: Find and support local suppliers of organic seeds, fertilizers, and other sustainable farming products through our curated marketplace. Promote small-scale producers and contribute to building resilient, community-based food systems.</li>
      </ul>

      <h4 class="mission">Our Commitment</h4>
      <ul>
        <li>Ecological Farming (eco-farming) ensures healthy farming and healthy food for today and tomorrow
by protecting soil, water, and climate, promoting biodiversity, and not contaminating the 
environment with chemical inputs or genetic engineering.
</li>
        <li>Education: We believe in the power of knowledge. We strive to educate and raise awareness about the benefits of eco-farming, equipping individuals with the tools they need to make informed decisions.</li>
        <li>Collaboration: We actively seek collaborations with organizations, researchers, and farmers to advance the field of eco-farming and create a network of support for sustainable agriculture.</li>
        <li>Innovation: We continuously explore and develop innovative technologies and methods to make eco-farming more accessible and efficient.</li>
      </ul>

